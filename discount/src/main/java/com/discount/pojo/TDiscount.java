package com.discount.pojo;


public class TDiscount {

  private Integer discountId;
  private Integer reduceId;
  private Integer userId;
  private Integer discountMark;


  public Integer getDiscountId() {
    return discountId;
  }

  public void setDiscountId(Integer discountId) {
    this.discountId = discountId;
  }


  public Integer getReduceId() {
    return reduceId;
  }

  public void setReduceId(Integer reduceId) {
    this.reduceId = reduceId;
  }


  public Integer getUserId() {
    return userId;
  }

  public void setUserId(Integer userId) {
    this.userId = userId;
  }


  public Integer getDiscountMark() {
    return discountMark;
  }

  public void setDiscountMark(Integer discountMark) {
    this.discountMark = discountMark;
  }

}
