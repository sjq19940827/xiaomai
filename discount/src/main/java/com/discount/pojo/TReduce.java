package com.discount.pojo;


public class TReduce {

  private Integer reduce_id;
  private String reduce_number;
  private String reduce_tot;
  private Integer sub;
  private String purpose;
  private String valid;


    public Integer getReduce_id() {
        return reduce_id;
    }

    public void setReduce_id(Integer reduce_id) {
        this.reduce_id = reduce_id;
    }

    public String getReduce_number() {
        return reduce_number;
    }

    public void setReduce_number(String reduce_number) {
        this.reduce_number = reduce_number;
    }

    public String getReduce_tot() {
        return reduce_tot;
    }

    public void setReduce_tot(String reduce_tot) {
        this.reduce_tot = reduce_tot;
    }

    public Integer getSub() {
        return sub;
    }

    public void setSub(Integer sub) {
        this.sub = sub;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getValid() {
        return valid;
    }

    public void setValid(String valid) {
        this.valid = valid;
    }
}
